package academy.day2;

public class OperationEx5 {

	public static void main(String[] args) {

		int num = 0B00000101;
		System.out.println("num = " + num);
		System.out.println("num << 2 = " + (num << 2));
		System.out.println("num >> 2 = " + (num >> 2));
		System.out.println("num >>> 2 = " + (num >>> 2));

	}

}
